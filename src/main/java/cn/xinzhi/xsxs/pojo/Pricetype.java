package cn.xinzhi.xsxs.pojo;

public class Pricetype {
    private Integer id;
    private Integer ptType;
    private Integer ptNum;
    private Double ptClassify;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getPtType() {
        return ptType;
    }

    public void setPtType(Integer ptType) {
        this.ptType = ptType;
    }

    public Integer getPtNum() {
        return ptNum;
    }

    public void setPtNum(Integer ptNum) {
        this.ptNum = ptNum;
    }

    public Double getPtClassify() {
        return ptClassify;
    }

    public void setPtClassify(Double ptClassify) {
        this.ptClassify = ptClassify;
    }
}
