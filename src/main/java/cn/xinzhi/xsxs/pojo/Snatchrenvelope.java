package cn.xinzhi.xsxs.pojo;

import java.util.Date;

public class Snatchrenvelope {
    private Integer id;
    private Integer srId;
    private Double srNum;
    private Integer srPid;
    private Date srTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSrId() {
        return srId;
    }

    public void setSrId(Integer srId) {
        this.srId = srId;
    }

    public Double getSrNum() {
        return srNum;
    }

    public void setSrNum(Double srNum) {
        this.srNum = srNum;
    }

    public Integer getSrPid() {
        return srPid;
    }

    public void setSrPid(Integer srPid) {
        this.srPid = srPid;
    }

    public Date getSrTime() {
        return srTime;
    }

    public void setSrTime(Date srTime) {
        this.srTime = srTime;
    }
}
